# Mayúsculas y minúsculas

texto = "Hola Mundo"

# Podemos usar el método upper para convertir una cadena de texto a mayúsculas
print(texto.upper())  # "HOLA MUNDO"

# También podemos usar el método lower para convertir una cadena de texto a minúsculas
print(texto.lower())  # "hola mundo"

# El método title convierte la primera letra de cada palabra a mayúscula y el resto a minúscula
print(texto.title())  # "Hola Mundo"    
