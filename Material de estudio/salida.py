# Una salida es lo que se muestra en la pantalla
print("Hola mundo")

# Podemos mostrar el valor de una variable en la pantalla usando la función print
numero = 10
print(numero)

# También podemos mostrar varias variables al mismo tiempo separándolas por comas
texto = "El número es"
print(texto, numero)

# Esta es la sintaxis de f-strings para formatear la salida concatenando texto con variables
print(f"El número es: {numero}")